"""
小红书爆款文案生成 Agent

功能：
- 根据用户输入的主题/产品/关键词，生成小红书风格的爆款文案
- 支持选择文案风格（种草/测评/教程/情感/干货）
- 支持指定目标受众和语气
- 自动生成标题、正文、标签、Emoji 表情

Run:
    agentclaw up  # 启动服务后可通过 API 调用
"""

import asyncio
from agentclaw import Workflow, LLMNode, Input

# ============================================================
# Workflow 定义
# ============================================================
workflow = Workflow(
    id="xiaohongshu_writer",
    name="小红书爆款文案生成器",
    description="根据主题/产品/关键词，自动生成小红书风格的爆款文案，支持多种风格和自定义参数。",
    version="1.0.0",
    welcome="📕 你好！我是小红书文案生成助手，告诉我你想推广的主题或产品，我来帮你写出爆款文案！",
    inputs=[
        Input("topic", str, required=True,
              description="文案主题或产品名称，例如：'这款美白精华液'、'周末北京周边游'"),
        Input("style", str, required=False, default="种草",
              description="文案风格：种草 / 测评 / 教程 / 情感 / 干货",
              choices=["种草", "测评", "教程", "情感", "干货"]),
        Input("target_audience", str, required=False, default="",
              description="目标受众，例如：'学生党'、'上班族'、'宝妈'（可选）"),
        Input("tone", str, required=False, default="",
              description="语气风格，例如：'幽默'、'真诚'、'专业'、'温暖'（可选）"),
        Input("key_points", str, required=False, default="",
              description="需要突出的卖点或关键信息，逗号分隔（可选）"),
        Input("word_count", str, required=False, default="300-500",
              description="期望字数范围，例如：'200-300'、'500-800'"),
    ],
    user_input="topic",
)

# ============================================================
# 系统提示词 - 小红书文案生成专家
# ============================================================
SYSTEM_PROMPT = """你是一位资深的小红书内容创作专家，精通小红书平台的爆款文案写作技巧。

## 你的核心能力
1. 根据用户提供的主题/产品，创作符合小红书风格的文案
2. 熟悉小红书的标题党技巧、emoji 使用、段落排版
3. 懂得如何制造"种草"效果，引发读者共鸣和购买欲

## 小红书爆款文案公式
一篇优秀的小红书文案包含以下要素：

### 1️⃣ 标题（必含）
- 使用数字、疑问句、痛点词、悬念词
- 例如："3天见效！这款美白精华真的绝了😱"
- 标题控制在 20 字以内，要让人忍不住点进去

### 2️⃣ 开头（抓人眼球）
- 用痛点/共鸣开场："作为一个干皮星人，我真的受够了..."
- 或者用惊喜感开场："姐妹们！我终于找到了..."
- 前 3 行决定读者是否继续看下去

### 3️⃣ 正文（干货+体验）
- 分点叙述，用 emoji 做视觉锚点
- 真实感最重要！不要像广告，要像朋友分享
- 每个段落不超过 3 行
- 适当加入个人体验和感受

### 4️⃣ 结尾（互动引导）
- 引导评论："姐妹们你们觉得怎么样？"
- 引导收藏："先收藏起来慢慢看～"
- 引导关注："关注我，更多好物分享！"

### 5️⃣ 标签（3-5个）
- 包含主题标签、人群标签、场景标签
- 例如：#美白精华 #学生党护肤 #好物分享

## 文案风格指南

### 种草风格
- 充满惊喜和赞叹的语气
- 多用"绝了"、"yyds"、"封神"、"太可了"等词
- 强调使用前后的对比效果
- 适合：美妆、穿搭、家居好物

### 测评风格
- 客观分析优缺点
- 使用评分/对比表格
- 强调真实体验，不吹不黑
- 适合：数码产品、护肤品、食品

### 教程风格
- 步骤清晰，分步讲解
- 强调"手把手"、"保姆级"
- 包含 tips 和小技巧
- 适合：化妆教程、烹饪、手工DIY

### 情感风格
- 故事化叙述，有代入感
- 引发情感共鸣
- 适合：情感经历、生活感悟、成长故事

### 干货风格
- 信息密度高，实用性强
- 条理清晰，总结归纳
- 适合：知识分享、职场技巧、学习方法

## 输出格式要求
请严格按照以下格式输出文案：

---

📌 **标题**：[标题内容]

---

💬 **文案正文**：

[正文内容，使用 emoji 分段]

---

🏷️ **推荐标签**：
[标签1] [标签2] [标签3] [标签4] [标签5]

---

📝 **文案说明**：
- 风格：[当前风格]
- 字数：[实际字数]
- 适用人群：[目标受众]
- 核心卖点：[主要卖点]
"""

# ============================================================
# LLM 节点
# ============================================================
workflow.add_node(LLMNode(
    id="content_generator",
    system_prompt=SYSTEM_PROMPT,
    user_prompt=(
        "请帮我生成一篇小红书文案。\n\n"
        "主题/产品：{topic}\n"
        "风格：{style}\n"
        "目标受众：{target_audience}\n"
        "语气风格：{tone}\n"
        "需要突出的卖点：{key_points}\n"
        "期望字数：{word_count}"
    ),
    stream=True,
    output_to_user=True,
))

workflow.publish()


if __name__ == "__main__":
    asyncio.run(workflow.run({
        "topic": "一款适合学生党的平价美白精华",
        "style": "种草",
        "target_audience": "学生党",
        "tone": "真诚",
        "key_points": "平价, 温和不刺激, 有效果, 国货",
        "word_count": "300-500",
    }))
